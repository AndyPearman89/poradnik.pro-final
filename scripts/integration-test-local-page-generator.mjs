#!/usr/bin/env node

/**
 * Integration Tests for LocalPageGenerator
 *
 * E2E tests for local page generation, canonical URLs,
 * meta tags, and schema.org rendering
 */

// ============================================================================
// TEST UTILITIES
// ============================================================================

const baseURL = new URL(process.argv[2] || 'http://127.0.0.1:8080');
let testsPassed = 0;
let testsFailed = 0;

async function test(name, fn) {
  try {
    await fn();
    console.log(`✓ ${name}`);
    testsPassed++;
  } catch (err) {
    console.log(`✗ ${name}`);
    console.log(`  Error: ${err.message}`);
    testsFailed++;
  }
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

async function makeRequest(path, options = {}) {
  const url = new URL(path, baseURL);
  const response = await fetch(url.toString(), options);
  const text = await response.text();
  return { status: response.status, body: text };
}

// ============================================================================
// TEST 1: Homepage is accessible
// ============================================================================

await test('Homepage is accessible', async () => {
  const response = await makeRequest('/');
  assert(response.status === 200, `Expected 200, got ${response.status}`);
  assert(response.body.length > 100, 'Expected HTML content');
});

// ============================================================================
// TEST 2: Homepage contains canonical tag
// ============================================================================

await test('Homepage contains canonical link tag', async () => {
  const response = await makeRequest('/');
  assert(response.body.includes('rel="canonical"'), 'Expected canonical link tag');
  assert(response.body.includes('href="'), 'Expected href attribute in canonical');
});

// ============================================================================
// TEST 3: Homepage contains meta description
// ============================================================================

await test('Homepage contains meta description', async () => {
  const response = await makeRequest('/');
  assert(response.body.includes('name="description"'), 'Expected meta description');
  assert(response.body.includes('content="'), 'Expected content attribute');
});

// ============================================================================
// TEST 4: Homepage contains schema.org markup
// ============================================================================

await test('Homepage contains JSON-LD schema markup', async () => {
  const response = await makeRequest('/');
  assert(response.body.includes('application/ld+json'), 'Expected JSON-LD script tag');
  assert(response.body.includes('@context'), 'Expected @context in schema');
  assert(response.body.includes('schema.org'), 'Expected schema.org namespace');
});

// ============================================================================
// TEST 5: Local page URL generation pattern
// ============================================================================

await test('Local page URLs follow /uslugi/service-location/ pattern', async () => {
  // This test validates the pattern locally (without actual page existence)
  // In real usage: /uslugi/prawo-rodzinne-warszawa/
  const localPageUrl = 'http://127.0.0.1:8080/uslugi/prawo-rodzinne-warszawa/';
  assert(localPageUrl.includes('/uslugi/'), 'Expected /uslugi/ prefix');
  assert(localPageUrl.includes('-'), 'Expected service-location separator');
  assert(!localPageUrl.includes('--'), 'Expected no double hyphens');
});

// ============================================================================
// TEST 6: Archive page contains ItemList schema
// ============================================================================

await test('Archive pages contain ItemList schema when available', async () => {
  const archivePaths = ['/ranking/', '/questions/', '/guides/'];
  
  for (const path of archivePaths) {
    try {
      const response = await makeRequest(path);
      if (response.status === 200) {
        assert(
          response.body.includes('ItemList') || response.body.includes('schema'),
          `Expected schema in ${path}`
        );
      }
    } catch {
      // Skip non-existent archive paths
    }
  }
});

// ============================================================================
// TEST 7: Schema contains proper areaServed structure
// ============================================================================

await test('LocalBusiness schema includes areaServed metadata', async () => {
  // Prepare expected schema structure (would be generated by LocalPageGenerator)
  const mockSchema = {
    '@context': 'https://schema.org',
    '@type': 'LocalBusiness',
    'areaServed': {
      '@type': 'City',
      'name': 'Warszawa'
    }
  };
  
  // Validate structure
  assert(mockSchema['@type'] === 'LocalBusiness', 'Expected LocalBusiness type');
  assert(mockSchema.areaServed['@type'] === 'City', 'Expected City type for areaServed');
  assert(mockSchema.areaServed.name, 'Expected city name in areaServed');
});

// ============================================================================
// TEST 8: OpenGraph tags structural validation
// ============================================================================

await test('Homepage contains required OpenGraph tags', async () => {
  const response = await makeRequest('/');
  const ogRequired = ['og:title', 'og:description', 'og:type'];
  
  for (const ogTag of ogRequired) {
    assert(
      response.body.includes(`property="${ogTag}"`),
      `Expected ${ogTag} OpenGraph tag`
    );
  }
});

// ============================================================================
// TEST 9: No canonical URL rewrite loops detected
// ============================================================================

await test('Canonical URLs do not create redirect loops', async () => {
  // Validate that canonical URL structure is deterministic
  const url1 = '/uslugi/prawo-rodzinne-warszawa/';
  const url2 = '/uslugi/prawo-rodzinne-warszawa/';
  
  assert(url1 === url2, 'Expected deterministic canonical URL generation');
  assert(!url1.includes('//'), 'Expected no double slashes in canonical');
});

// ============================================================================
// TEST 10: Metadata consistency across pages
// ============================================================================

await test('Page metadata follows consistent patterns', async () => {
  const response = await makeRequest('/');
  const patterns = {
    title: response.body.includes('<title>'),
    canonical: response.body.includes('rel="canonical"'),
    description: response.body.includes('name="description"'),
    schema: response.body.includes('application/ld+json')
  };
  
  const totalPatterns = Object.values(patterns).filter(p => p).length;
  assert(totalPatterns >= 3, `Expected at least 3 SEO patterns, found ${totalPatterns}`);
});

// ============================================================================
// RESULTS
// ============================================================================

console.log('\n==============================');
console.log(`Results: ${testsPassed}/${testsPassed + testsFailed} passed`);

if (testsFailed > 0) {
  console.log(`FAILED: ${testsFailed} test(s) failed`);
  process.exit(1);
} else {
  console.log('Overall: PASS');
  process.exit(0);
}
